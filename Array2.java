package arrays;

public class Array2 {

	public static void main(String[] args) {
		
		int [] ar = new int [3];
		System.out.println(ar[0]);
		System.out.println(ar[1]);
		System.out.println(ar[2]);
		/*
		 * System.out.println(ar[3]);
		   System.out.println(ar[-1]);
		   
		   	WE CAN NOT PRINT INDEX OUT OF LENGTH
		 */
		
		boolean [] ar2 = new boolean [4];
		System.out.println(ar2[0]);
		System.out.println(ar2[1]);
		System.out.println(ar2[2]);
		System.out.println(ar2[3]);
		
		
		float [] ar3 = new float [3];
		System.out.println(ar3[0]);
		System.out.println(ar3[2]);
		System.out.println(ar3[1]);
		
		String [] ar4 = new String [4];
		System.out.println(ar4[0]);
		System.out.println(ar4[1]);
		System.out.println(ar4[3]);
		System.out.println(ar4[2]);
		System.out.println(ar4[2]);
		
		
		char [] ar5 = new char [2];
		System.out.println(ar5[0]);
		System.out.println(ar5[1]);
		
		
		
		
		
		
	}
}
